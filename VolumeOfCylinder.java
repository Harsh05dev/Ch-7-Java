public class VolumeOfCylinder

{ 
         public static void main(String args[])
             {
                 double pi,r,h,v;
                 pi=3.14;
                 r=2;
                 h=3;
                 v=pi*r*r*h;
                 
                 System.out.println("Radius Of Cylinder is "+r);
                 System.out.println("Height Of Cylinder is "+h);
                 System.out.println("Volume Of Cylinder is "+v);
      
             }
}